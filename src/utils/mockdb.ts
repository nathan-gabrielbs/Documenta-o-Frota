// Helper to format YYYY-MM-DD to DD/MM/YYYY exactly without timezone conversion
export function formatarDataBR(dateStr: string | null | undefined): string {
  if (!dateStr) return '';
  const clean = dateStr.trim();
  if (clean === '') return '';
  
  // If it's already in DD/MM/YYYY format, return as is
  if (/^\d{2}\/\d{2}\/\d{4}$/.test(clean)) return clean;
  
  // If it has ISO time (e.g. 2026-06-22T00:00:00Z) or is a simple YYYY-MM-DD
  const dateOnly = clean.split('T')[0];
  const parts = dateOnly.split('-');
  if (parts.length === 3) {
    const [year, month, day] = parts;
    return `${day}/${month}/${year}`;
  }
  return clean;
}

// Helper to determine automatic document status relative to reference date 2026-06-22
export function calcularStatusDocumento(aplicavel: boolean, dataVencimentoStr: string): StatusDocumento {
  if (!aplicavel) return 'Não aplicável';
  if (!dataVencimentoStr || dataVencimentoStr.trim() === '') return 'Vencido';
  
  const refDate = new Date('2026-06-22');
  const expDate = new Date(dataVencimentoStr);
  
  if (isNaN(expDate.getTime())) return 'Vencido';
  
  // Set times to midnight to avoid hours differences
  refDate.setHours(0, 0, 0, 0);
  expDate.setHours(0, 0, 0, 0);
  
  const diffTime = expDate.getTime() - refDate.getTime();
  const diffDays = Math.ceil(diffTime / (1000 * 60 * 60 * 24));
  
  if (diffDays < 0) {
    return 'Vencido';
  } else if (diffDays >= 0 && diffDays <= 30) {
    return 'Crítico';
  } else if (diffDays >= 31 && diffDays <= 60) {
    return 'Atenção';
  } else {
    return 'Válido';
  }
}

