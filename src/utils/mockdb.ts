export function isDocumentApplicable(tipoUnidade: string, tipoDocumento: string): boolean {
  const tDoc = tipoDocumento.toUpperCase().trim();
  const tUnit = tipoUnidade ? tipoUnidade.trim() : '';
  const isMangote = tDoc === 'LAUDO MANGOTE' || tDoc === 'LAUDO DE MANGOTE';

  if (tUnit === 'Cavalo') {
    // Cavalo é obrigatório apenas: CIV, TACÓGRAFO E LAUDO QUINTA RODA
    return tDoc === 'CIV' || tDoc === 'TACÓGRAFO' || tDoc === 'LAUDO QUINTA RODA';
  } else if (tUnit === 'Carreta') {
    // Carreta é obrigatório apenas: CIV, CIPP, INMETRO, LAUDO DE BOTTOM e LAUDO DE MANGOTE
    return tDoc === 'CIV' || tDoc === 'CIPP' || tDoc === 'INMETRO' || tDoc === 'LAUDO DE BOTTOM' || isMangote;
  } else if (tUnit === 'Porta Container') {
    // Porta Container é obrigatório apenas CIV
    return tDoc === 'CIV';
  } else {
    // For other units (e.g. Truck, Toco, Bitruck, Outro), keep all applicable EXCEPT LAUDO QUINTA RODA which is strictly for Cavalo.
    if (tDoc === 'LAUDO QUINTA RODA') return false;
    return true;
  }
}

