export function saveDocumentsToLocalStorage(docs: Documento[]) {
  docs.forEach(d => {
    if (d.arquivoAnexoConteudo) {
      try {
        localStorage.setItem(STORAGE_PREFIX + 'file_' + d.id, d.arquivoAnexoConteudo);
      } catch (err) {
        console.error("Failed to save base64 content for document " + d.id, err);
      }
    } else {
      localStorage.removeItem(STORAGE_PREFIX + 'file_' + d.id);
    }
  });
  
  // Strip base64 content from the main array so that it's tiny
  const stripped = docs.map(d => ({
    ...d,
    arquivoAnexoConteudo: undefined
  }));
  
  try {
    localStorage.setItem(STORAGE_PREFIX + 'documentos', JSON.stringify(stripped));
  } catch (err) {
    console.error("Failed to save documents array to LocalStorage", err);
  }
}

export function loadDocumentsFromLocalStorage(seed: Documento[] = SEED_DOCUMENTS): Documento[] {
  const s = localStorage.getItem(STORAGE_PREFIX + 'documentos');
  if (!s) {
    return seed;
  }
  try {
    const parsed = JSON.parse(s) as Documento[];
    // Reconstruct the base64 content from separate keys if they exist
    parsed.forEach(d => {
      const fileContent = localStorage.getItem(STORAGE_PREFIX + 'file_' + d.id);
      if (fileContent) {
        d.arquivoAnexoConteudo = fileContent;
      }
    });
    return parsed;
  } catch {
    return seed;
  }
}

