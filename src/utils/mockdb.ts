export function handleFirestoreError(error: unknown, operationType: OperationType, path: string | null): void {
  const errStr = error instanceof Error ? error.message : String(error);
  const isQuota = errStr.toLowerCase().includes('quota') || 
                  errStr.toLowerCase().includes('resource-exhausted') ||
                  errStr.toLowerCase().includes('exhausted') ||
                  errStr.toLowerCase().includes('rate limit') ||
                  errStr.toLowerCase().includes('overload') ||
                  errStr.toLowerCase().includes('billing');

  if (isQuota) {
    activateLocalOnlyMode();
    // Do not crash the application when cloud quota is reached. Log nicely and fall back to Local Storage.
    console.warn('Firestore Quota Limit Met. Gracefully switching to Offline Local Mode:', errStr);
    return;
  }

  const errInfo: FirestoreErrorInfo = {
    error: errStr,
    authInfo: {
      userId: null,
      email: null,
      emailVerified: null,
      isAnonymous: null,
      tenantId: null,
      providerInfo: []
    },
    operationType,
    path
  };
  console.error('Firestore Error: ', JSON.stringify(errInfo));
  throw new Error(JSON.stringify(errInfo));
}

export function cleanUndefined<T>(obj: T): T {
  if (obj === null || typeof obj !== 'object') {
    return obj;
  }
  if (Array.isArray(obj)) {
    return obj.map(cleanUndefined) as unknown as T;
  }
  const result: any = {};
  for (const key of Object.keys(obj as any)) {
    const val = (obj as any)[key];
    if (val !== undefined) {
      result[key] = cleanUndefined(val);
    }
  }
  return result as T;
}

