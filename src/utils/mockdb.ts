function isValidDate(dateStr: string | undefined | null): boolean {
  if (!dateStr) return false;
  const s = dateStr.trim();
  if (s === '' || s === '1900-01-00' || s.startsWith('1900-')) return false;
  return /^\d{4}-\d{2}-\d{2}$/.test(s);
}

function parseDDMMYYYYtoYYYYMMDD(dateStr: string | undefined | null): string {
  if (!dateStr) return '';
  const clean = dateStr.trim();
  if (clean === '') return '';
  if (/^\d{4}-\d{2}-\d{2}$/.test(clean)) {
    return clean;
  }
  const parts = clean.split('/');
  if (parts.length === 3) {
    const day = parts[0].padStart(2, '0');
    const month = parts[1].padStart(2, '0');
    const year = parts[2];
    if (year.length === 4 && day.length === 2 && month.length === 2) {
      return `${year}-${month}-${day}`;
    }
  }
  return '';
}

function calculateEmissionDate(expirationDate: string): string {
  if (!isValidDate(expirationDate)) return '';
  const parts = expirationDate.split('-');
  const year = parseInt(parts[0], 10);
  return `${year - 1}-${parts[1]}-${parts[2]}`;
}

